#[derive(Debug, PartialEq, Clone)]
pub enum Token {
    Fn,
    Return,
    Comma,
    Ident(String),
    Integer(i64),
    Illegal(char),
    Assign,
    Semicolon,
    LParen,
    RParen,
    Let,
    Print,
    Plus,
    Minus,
    Star,
    Slash,
    Modulo,
    If,
    Else,
    While,
    True,
    False,
    Eq,
    NotEq,
    Lt,
    Gt,
    LBrace,
    RBrace,
    EOF,
}
